    private void DeleteBTNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DeleteBTNActionPerformed
        Student s = StudentSystemDCBSDPUEntityManager.find(Student.class, SIDtxt.getText());
        
        int res=JOptionPane.showConfirmDialog(this, s.getSid(), "Delete ?", 1);
                if(res==JOptionPane.OK_OPTION){
                        StudentSystemDCBSDPUEntityManager.getTransaction().begin();
                        StudentSystemDCBSDPUEntityManager.remove(s);
                        StudentSystemDCBSDPUEntityManager.getTransaction().commit();
                        JOptionPane.showConfirmDialog(this, s.getSid()+"Delete?");
                }
                studentList.clear();
                studentList=studentQuery.getResultList();
                refreshTable(studentList);
    }//GEN-LAST:event_DeleteBTNActionPerformed

