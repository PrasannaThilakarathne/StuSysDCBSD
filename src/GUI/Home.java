    private void searchtxtKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_searchtxtKeyReleased
        if(IDRDBTN.isSelected()){
            Query qid = StudentSystemDCBSDPUEntityManager.createQuery("SELECT s FROM Student s WHERE s.sid like :sid");
            qid.setParameter("sid", "%"+searchtxt.getText()+"%");
            
            studentList.clear();
            studentList = qid.getResultList();
            refreshTable(studentList);
        }
        else if(NameRBTN.isSelected()){
            Query qname = StudentSystemDCBSDPUEntityManager.createQuery("SELECT s FROM Student s WHERE s.name like :name");
            qname.setParameter("name", "%"+searchtxt.getText()+"%");
            
            studentList.clear();
            studentList = qname.getResultList();
            refreshTable(studentList);
        }
        else if(AddressRBTN.isSelected()){
            Query qadd = StudentSystemDCBSDPUEntityManager.createQuery("SELECT s FROM Student s WHERE s.address like :address");
            qadd.setParameter("address", "%"+searchtxt.getText()+"%");
            
            studentList.clear();
            studentList = qadd.getResultList();
            refreshTable(studentList);
        }
        else{
            JOptionPane.showMessageDialog(this, "Select a Search Critaria!");
        }
    }//GEN-LAST:event_searchtxtKeyReleased

    private void DepSumBTNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DepSumBTNActionPerformed
        List<Stu_Dep> depSum = new ArrayList<>();
        depSum.add(new Stu_Dep(0, "MM"));
        depSum.add(new Stu_Dep(0, "HR"));
        depSum.add(new Stu_Dep(0, "IT"));
        
        for (Student st : studentList) {
            switch(st.getDid().getDepartmentName())
            {
                case "MM" : depSum.get(0).setCount(depSum.get(0).getCount()+1);break;
                case "HR" : depSum.get(1).setCount(depSum.get(1).getCount()+1);break;
                case "IT" : depSum.get(2).setCount(depSum.get(2).getCount()+1);break;
                default:break;
            }
        }
        org.jdesktop.swingbinding.JTableBinding jTableBinding = org.jdesktop.swingbinding.SwingBindings.createJTableBinding(org.jdesktop.beansbinding.AutoBinding.UpdateStrategy.READ, depSum, jTable3);
        org.jdesktop.swingbinding.JTableBinding.ColumnBinding columnBinding = jTableBinding.addColumnBinding(org.jdesktop.beansbinding.ELProperty.create("${depName}"));
        columnBinding.setColumnName("Department Name");
        columnBinding.setColumnClass(String.class);
        columnBinding = jTableBinding.addColumnBinding(org.jdesktop.beansbinding.ELProperty.create("${count}"));
        columnBinding.setColumnName("Number OF Students");
        columnBinding.setColumnClass(Integer.class);
        
        bindingGroup.addBinding(jTableBinding);
        jTableBinding.bind();
    }//GEN-LAST:event_DepSumBTNActionPerformed

