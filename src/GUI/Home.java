    private void InsertBTNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_InsertBTNActionPerformed
                Student s = new Student();
                s.setSid(SIDtxt.getText());
                s.setName(Snametxt.getText());
                s.setAge(Integer.parseInt(Agetxt.getText()));
                s.setAddress(adresstxt.getText());
                s.setDid((Department)DepartmntCMB.getSelectedItem());
        
                int res=JOptionPane.showConfirmDialog(this, s.getSid(), "insert ?", 1);
                if(res==JOptionPane.OK_OPTION){
                    try {
                        StudentSystemDCBSDPUEntityManager.getTransaction().begin();
                        StudentSystemDCBSDPUEntityManager.persist(s);
                        StudentSystemDCBSDPUEntityManager.getTransaction().commit();
                        JOptionPane.showConfirmDialog(this, s.getSid()+"Insert ?");
                    } catch (Exception e) {
                        System.out.println("Found Duplicate SID!!!");
                       JOptionPane.showMessageDialog(this, "Please Provide defferent Student ID from existings");
                    }
                       
                       
                    }
                studentList.clear();
                studentList=studentQuery.getResultList();
                refreshTable(studentList);
    }//GEN-LAST:event_InsertBTNActionPerformed

